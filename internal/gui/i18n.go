func t(state *AppState, key string) string {
	state.mu.RLock()
	lang := state.Language
	state.mu.RUnlock()
	if lang == "zh-CN" {
		if v, ok := zhCN[key]; ok {
			return v
		}
	} else {
		if v, ok := enUS[key]; ok {
			return v
		}
	}
	return key
}
